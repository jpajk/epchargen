<?php

namespace CharacterBundle\Entity\Skill;

use Doctrine\ORM\Mapping as ORM;

/**
 * BaseAbility
 *
 * @ORM\Table(name="skill")
 * @ORM\Entity(repositoryClass="CharacterBundle\Repository\Skill\BaseAbilityRepository")
 */
class Skill
{
    /**
     * @var int
     *
     * @ORM\Column(name="id", type="integer")
     * @ORM\Id
     * @ORM\GeneratedValue(strategy="AUTO")
     */
    private $id;

    /**
     * @var string
     *
     * @ORM\Column(name="name", type="string", length=255, unique=true)
     */
    private $name;

    /**
     * @ORM\OneToMany(targetEntity="SkillValue", mappedBy="skill")
     */
    private $skillValue;

    /**
     * Get id
     *
     * @return int
     */
    public function getId()
    {
        return $this->id;
    }

    /**
     * Set name
     *
     * @param string $name
     *
     * @return Skill
     */
    public function setName($name)
    {
        $this->name = $name;

        return $this;
    }

    /**
     * Get name
     *
     * @return string
     */
    public function getName()
    {
        return $this->name;
    }
}

